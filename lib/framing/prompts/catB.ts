/**
 * lib/framing/prompts/catB.ts
 *
 * Pass 2 — Category B: Completeness checks B1–B13.
 *
 * Checks whether all required framing sections and elements are present.
 * Status values: "PASS" | "FAIL" | "NA"
 *
 * Special rules:
 * - B9 (Decision-Maker Identification) automatically FAILS if typology is null,
 *   because without a declared typology the decision authority cannot be assessed.
 * - B4 (Success Criteria Presence) is N/A if typology is null.
 * - Client-Stated Input Protocol applies to B13 (Capital/Resource Ask Quantification).
 */

import type { FramingSection, PromptPayload } from "../types";

// ---------------------------------------------------------------------------
// Check definitions — all B1–B13
// ---------------------------------------------------------------------------

const CAT_B_CHECKS = `
CHECK DEFINITIONS — Category B: Completeness

Checks in this category produce status "PASS", "FAIL", or "NA" only.

B1 — Decision Question Presence
WHAT TO CHECK: Does the framing contain an explicitly stated decision question (a single interrogative sentence asking what decision is to be made)?
FAIL if: No decision question is present anywhere in the document.
Never N/A.
IMPACT: The downstream memo cannot be scored for Problem Formulation fidelity (Pillar 2) without a stated decision question.

B2 — Blocking Questions Enumeration
WHAT TO CHECK: Does the framing list blocking questions — sub-questions whose answers must appear in the memo before the main decision can be made?
FAIL if: No blocking questions are listed.
Never N/A.
IMPACT: Without blocking questions, the Gap-filling sub-dimension of Pillar 2 cannot be scored.

B3 — Constraints Section Presence
WHAT TO CHECK: Does the framing contain a Constraints section?
FAIL if: No constraints section is present.
N/A if: The framing is explicitly designated as an early-stage conceptual framing where constraints are genuinely undefined (must be stated explicitly in the framing).
IMPACT: Without stated constraints, the memo's Executability sub-dimension (Pillar 2) cannot be fully evaluated.

B4 — Success Criteria Presence
WHAT TO CHECK: Does the framing contain success criteria — a definition of what "Go" looks like?
FAIL if: No success criteria are present.
N/A if: typology is null (cannot assess typology-appropriate success criteria without a declared typology).
Never otherwise N/A.
IMPACT: Without success criteria, the memo cannot address the decision's positive threshold.

B5 — Kill Conditions Presence
WHAT TO CHECK: Does the framing contain kill conditions — a definition of what "No-Go" looks like?
FAIL if: No kill conditions are present.
N/A if: The framing is explicitly designated as a purely exploratory pre-decision feasibility document.
IMPACT: Without kill conditions, the memo cannot address the decision's negative threshold.

B6 — Assumptions Section Presence
WHAT TO CHECK: Does the framing contain an explicit assumptions section?
FAIL if: No assumptions section is present.
N/A if: The framing was generated by the Innovera Wizard (auto-populates assumptions — check for Wizard metadata).
IMPACT: The downstream memo's Assumption Quality pillar (P6) cannot be fully evaluated without framing-stated assumptions.

B7 — Risks Section Presence
WHAT TO CHECK: Does the framing contain a risks section?
FAIL if: No risks section is present.
Never N/A.
IMPACT: The Risk Review Gate cannot be properly seeded without framing-identified risks.

B8 — Typology Declaration
WHAT TO CHECK: Does the framing declare a typology using one of the four canonical labels: 1A External Investment / 1B Internal Initiative / 2A New Market Entry / 2B New Product Launch?
FAIL if: No typology is declared OR the declared typology does not use one of the four canonical labels.
Never N/A.
IMPACT: Without a typology declaration, Pillar 3 Structural Accuracy cannot be scored because the expected chapter list is typology-dependent.

B9 — Decision-Maker Identification
WHAT TO CHECK: Does the framing identify who makes the final decision (IC, Board, specific executive role)?
FAIL if: No decision-maker is identified AND typology is null (compounded ambiguity).
FAIL if: No decision-maker is identified for typologies 1A or 1B (investment and initiative decisions require named authority).
N/A if: The framing is explicitly internal to a single named individual and the decision authority is that individual.
IMPACT: Without a named decision authority, the memo cannot be evaluated for decision-utility relevance.

B10 — Timeline / Decision Date Presence
WHAT TO CHECK: Does the framing contain a decision date or decision-required-by window?
FAIL if: No decision date or window is stated.
Never N/A.
IMPACT: Without a decision date, the memo's time-bounded recommendations cannot be assessed.

B11 — Scope Boundary Statement
WHAT TO CHECK: Does the framing contain an explicit scope boundary statement (what is in scope and what is out of scope)?
FAIL if: No scope boundary is stated.
N/A if: The framing is a narrow single-topic framing where scope is implicit and unambiguous given the decision question.
IMPACT: Without a scope statement, the memo author cannot determine which topics to include or exclude.

B12 — Stakeholder Map Presence
WHAT TO CHECK: Does the framing contain a stakeholder map identifying who is affected by the decision?
FAIL if: No stakeholders are identified.
N/A if: The framing is a single-entity framing with a clear, undisputed and trivially obvious stakeholder set.
IMPACT: Without a stakeholder map, the memo cannot address the interests of affected parties.

B13 — Capital / Resource Ask Quantification
WHAT TO CHECK: Does the framing involve a capital or resource commitment but contain no quantified ask?
FAIL if: The decision clearly involves a capital or resource commitment (language like "invest", "fund", "allocate", "resource") but no budget range, headcount request, or resource envelope is stated.
N/A if: The framing is explicitly exploratory with no capital decision being made.
N/A if: A client-stated figure is present — Client-Stated Input Protocol: if the client has provided any resource figure (even a rough estimate or range), treat B13 as PASS regardless of specificity.
IMPACT: Without a quantified resource ask, Pillar 8 Specificity sub-dimension cannot be properly evaluated.
`.trim();

// ---------------------------------------------------------------------------
// Builder
// ---------------------------------------------------------------------------

export function buildCatBPrompt(
  framingText: string,
  sections: FramingSection[],
  typology: "1A" | "1B" | "2A" | "2B" | null,
): PromptPayload {
  const sectionsJson = JSON.stringify(sections, null, 2);
  const typologyStr = typology ?? "null — typology was not detected in Pass 0";

  const systemPrompt = `You are an Innovera Eval V3 Decision Framing Checker running Pass 2: Category B Completeness checks.

${CAT_B_CHECKS}

EVALUATOR ID: "primary"

TYPOLOGY-DEPENDENT RULES (apply these before running checks):
- If typology is null: B4 status must be "NA"; B9 must FAIL if no decision-maker is present.
- If typology is "1A" or "1B": B9 FAILS if no decision-maker is named.

CLIENT-STATED INPUT PROTOCOL (applies to B13):
Any capital figure, resource estimate, or budget range provided by the client must be treated as present for B13 purposes. Do not fail B13 because a client-stated figure lacks precision or methodology.

ARCHITECTURAL RULES:
1. Valid statuses: "PASS", "FAIL", "NA" only. Never "ADVISORY".
2. confidence: float 0–1. Use 0.9+ for unambiguous presence/absence. Use 0.6–0.8 for ambiguous cases.
3. location: quote the text that satisfies the check (for PASS), or the closest relevant text (for FAIL). If the issue is pure absence, set location to null.
4. issue and impact: null when status is PASS or NA.
5. rewrite: null when status is PASS or NA. When FAIL, provide a structural suggestion that does NOT prescribe analytical methodology or specific values.
6. Return ONLY a valid JSON object. No prose, no markdown fences.

RESPONSE SCHEMA:
{
  "category": "B",
  "results": [
    {
      "checkId": string,
      "evaluator": "primary",
      "status": "PASS" | "FAIL" | "NA",
      "confidence": number,
      "location": string | null,
      "issue": string | null,
      "impact": string | null,
      "rewrite": string | null
    }
  ]
}

Emit exactly 13 result objects, one per check B1 through B13, in order.`;

  const userPrompt = `TYPOLOGY (from Pass 0): ${typologyStr}

EXTRACTED SECTIONS (from Pass 0):
${sectionsJson}

FULL FRAMING DOCUMENT:
---
${framingText}
---

Run all Category B checks (B1–B13) and return the JSON result object.`;

  return {
    messages: [
      { role: "system", content: systemPrompt },
      { role: "user", content: userPrompt },
    ],
    responseSchema:
      '{ category: "B", results: FramingCheckResult[13] }',
  };
}
